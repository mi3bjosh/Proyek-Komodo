<h1>Selamat datang di Halaman Dosen !</h1>
          <p>Pada Halaman Dosen disediakan fasilitas sebagai berikut :</p><br>
          <p>1. Modul Materi</p>
          <ul>
            <li type="disc">Dosen bisa menambahkan Materi Perkuliahan yang akan ditampilkan di halaman mahasiswa.</li>
            <li type="disc">Materi yang ditampilkan akan disimpan secara berkala dan akan diberikan latihan soal.</li>
          </ul>
          <p>2. Modul Soal</p>
          <ul>
            <li type="disc">Dosen bisa menambahkan soal di halaman mahasiswa yang akan digunakan untuk mereview pemahaman mahasiswa terhadap materi yang diberikan.</li>
            <li type="disc">Hak akses manajemen latihan soal dipegang oleh user.</li>
          </ul>
          <p>3. Event Kalender</p>
          <ul>
            <li type="disc">Dosen bisa membuat jadwal kegiatan di kolom Event Kalendar.</li>
            <li type="disc">Dosen bisa menginformasikan kepada dosen lain mengenai kegiatan mata kuliah yang akan dilaksanakan.</li>
          </ul>	
          <br />
